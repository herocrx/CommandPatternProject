public class Light {

    public Light() {
    }

    public void on()
    {
        System.out.println("Light is turned on");
    }

    public void off()
    {
        System.out.println("Light is turned off");
    }

    public void up()
    {
        System.out.println("Light is shining brighter");
    }

    public void down()
    {
        System.out.println("Light is shining darker");
    }


}
